#!/usr/bin/python3

'''
"math" is a module, and methods can only be accessed by
referencing the module because of the "import" statement
'''

import math

print("ceil(4.4) = ", math.ceil(4.4))  # Round up
print("floor(4.4) = ", math.floor(4.4))  # Round down
print("fabs(-4.4) = ", math.fabs(-4.4))  # Absolute value
print("factorial(4) = ", math.factorial(4))  # Factorial
print("fmods(5, 4) = ", math.fmod(5, 4))  # Modulus
print("trunc(4.2) = ", math.trunc(4.2))  # Truncate
print("pow(2, 2) = ", math.pow(2, 2))  # Power
print("sqrt(4) = ", math.sqrt(4))  # Square root
print("math.e = ", math.e)  # e value
print("math.pi = ", math.pi)  # Pi value
print("exp(4) = ", math.exp(4))  # Exponent
print("log(20) = ", math.log(20))  # Natural Logarithm
print("log(1000, 10) = ", math.log(1000, 10))  # Logarithm specifying the base
print("log10(1000) = ", math.log10(1000))  # Logarithm to base 10
print("degrees(1.5708) = ", math.degrees(1.5708))  # Radians to Degrees
print("radians(90) = ", math.radians(90))  # Degrees to Radians
